public class Player {
    int x,y;

    /**
     * metodo getX
     * @return x
     */
    public int getX() {
        return x;
    }

    /**
     * metodo setX
     * @param x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * metodo getY
     * @return y
     */
    public int getY() {
        return y;
    }

    /**
     * metodo setY
     * @param y
     */
    public void setY(int y) {
        this.y = y;
    }
}
